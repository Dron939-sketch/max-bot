function answerStage2(questionIndex, level, measures) {
    const current = app.testData.stage2.current;
    const perceptionType = app.testData.perceptionType;
    const question = getStage2Question(perceptionType, current);
    
    app.testData.stage2.answers.push({
        question: current,
        level: level,
        text: question.options[level]
    });
    
    if (measures === "thinking") {
        app.testData.stage2.levelScores[level] = (app.testData.stage2.levelScores[level] || 0) + 1;
    }
    
    if (["СБ", "ТФ", "УБ", "ЧВ"].includes(measures)) {
        app.testData.stage2.strategyLevels[measures].push(parseInt(level));
    }
    
    app.testData.stage2.current++;
    
    saveToStorage(STORAGE_KEYS.TEST_PROGRESS, app.testData);
    
    if (app.testData.stage2.current >= app.testData.stage2.total) {
        finishStage2();
    } else {
        showScreen('stage2');
    }
}

