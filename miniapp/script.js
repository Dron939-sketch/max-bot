function calculateProgress(current, total) {
    const percent = Math.floor((current / total) * 10);
    const bar = "█".repeat(percent) + "░".repeat(10 - percent);
    return `▸ Вопрос ${current}/${total} • ${bar}`;
}

function determinePerceptionType(scores) {
    const external = scores.EXTERNAL || 0;
    const internal = scores.INTERNAL || 0;
    const symbolic = scores.SYMBOLIC || 0;
    const material = scores.MATERIAL || 0;
    
    const attention = external > internal ? "EXTERNAL" : "INTERNAL";
    const anxiety = symbolic > material ? "SYMBOLIC" : "MATERIAL";
    
    const key = `${attention}_${anxiety}`;
    return PERCEPTION_TYPES[key] || "СОЦИАЛЬНО-ОРИЕНТИРОВАННЫЙ";
}

function calculateThinkingLevel(levelScores) {
    const totalScore = Object.values(levelScores).reduce((a, b) => a + b, 0);
    
    if (totalScore <= 10) return 1;
    if (totalScore <= 20) return 2;
    if (totalScore <= 30) return 3;
    if (totalScore <= 40) return 4;
    if (totalScore <= 50) return 5;
    if (totalScore <= 60) return 6;
    if (totalScore <= 70) return 7;
    if (totalScore <= 80) return 8;
    return 9;
}

function getLevelGroup(level) {
    if (level <= 3) return "1-3";
    if (level <= 6) return "4-6";
    return "7-9";
}

function calculateFinalLevel(stage2Level, stage3Scores) {
    if (!stage3Scores || stage3Scores.length === 0) return stage2Level;
    const avgBehavior = stage3Scores.reduce((a, b) => a + b, 0) / stage3Scores.length;
    return Math.round((stage2Level + avgBehavior) / 2);
}

function determineDominantDilts(diltsCounts) {
    if (!diltsCounts || Object.keys(diltsCounts).length === 0) return "BEHAVIOR";
    return Object.entries(diltsCounts).reduce((a, b) => a[1] > b[1] ? a : b)[0];
}

